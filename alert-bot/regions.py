# Стабильный порядок и короткие числовые id для callback_data
# (callback_data в Telegram ограничена 64 байтами, названия туда не влезают).
REGION_LIST = list(REGIONS.keys())
REGION_ID = {name: i for i, name in enumerate(REGION_LIST)}


def display_name(canonical: str) -> str:
    """Отображаемое имя региона; незнакомые (районы/громады) показываем как есть."""
    return REGIONS.get(canonical, canonical)


def normalize(name: str) -> str | None:
    """Привести название региона из API к каноническому ключу."""
    name = (name or "").strip()
    if name in REGIONS:
        return name
    # Некоторые источники пишут "Киев" без "м." или с лишними пробелами
    lowered = name.lower()
    for canonical in REGIONS:
        if canonical.lower() == lowered:
            return canonical
    if "київ" in lowered and "область" not in lowered:
        return "м. Київ"
    if "севастопол" in lowered:
        return "м. Севастополь"
    if "крим" in lowered or "крым" in lowered:
        return "Автономна Республіка Крим"
    return None
